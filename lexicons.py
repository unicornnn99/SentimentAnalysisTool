# ----------------- Bangla Lexicons -----------------
def load_bnlex(vulgar_file, pos_file, neg_file, bnlex):
    with open(vulgar_file, encoding="utf-8") as f:
        vulgar_words = set(w.strip() for w in f if w.strip())
    with open(pos_file, encoding="utf-8") as f:
        bn_pos = set(w.strip() for w in f if w.strip())
    with open(neg_file, encoding="utf-8") as f:
        bn_neg = set(w.strip() for w in f if w.strip())
    bnlex.update({w:1 for w in bn_pos} )
    bnlex.update({w:-1 for w in bn_neg})
    return bnlex, vulgar_words


